// Condational operator (if)
// Check the given number is eEven or Odd...
#include<iostream>
using namespace std;
int main(){
	int num;
	cout<<"Enter a number please"<<endl;
	cin>>num;
	if(num%2==0){
		cout<<" Given Number is even"<<endl;
	}
	if(num%2!=0){
		cout<<"Given Number is odd"<<endl;
	}
	cout<<"THANKYOU";
	return 0;
	
}
